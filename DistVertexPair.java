
public class DistVertexPair {
    
    
    private int dist;
    private int vertex;
    
    
    public DistVertexPair(int d, int v)
    {
        dist = d;
        vertex = v;
    }
    
    public int getDist()
    {
        return dist;
    }
    
    public void setDist(int d)
    {
        dist = d;
    }
    
    public int getVertex()
    {
        return vertex;
    }
    
    public void setVertex(int v)
    {
        vertex = v;
    }
    
    
    
    

}
